namespace DomainRelay.Mapping.Expressions.Dynamic;

/// <summary>
/// Represents a dynamic sort direction.
/// </summary>
public enum DynamicSortDirection
{
    /// <summary>
    /// Sorts values in ascending order.
    /// </summary>
    Asc = 0,

    /// <summary>
    /// Sorts values in descending order.
    /// </summary>
    Desc = 1
}